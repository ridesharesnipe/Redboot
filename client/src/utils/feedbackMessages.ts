// Track recently used messages to avoid immediate repeats
let recentMessages: string[] = [];

export function getFeedback(gradeLevel: string | null, isCorrect: boolean, isRetryAttempt: boolean = false): string {
  // Normalize grade level
  const normalizedGrade = normalizeGradeLevel(gradeLevel);
  
  // Get appropriate message array
  let messages: string[];
  if (isCorrect) {
    messages = feedbackMessages[normalizedGrade].correct;
  } else if (isRetryAttempt) {
    messages = feedbackMessages[normalizedGrade].wrongRetry;
  } else {
    messages = feedbackMessages[normalizedGrade].wrongFirstAttempt;
  }
  
  // Filter out recently used messages (last 5)
  const availableMessages = messages.filter(msg => !recentMessages.includes(msg));
  
  // If all messages used recently, reset pool
  const messagePool = availableMessages.length > 0 ? availableMessages : messages;
  
  // Random selection
  const selectedMessage = messagePool[Math.floor(Math.random() * messagePool.length)];
  
  // Track usage
  recentMessages.push(selectedMessage);
  if (recentMessages.length > 5) {
    recentMessages.shift(); // Keep only last 5
  }
  
  return selectedMessage;
}

function normalizeGradeLevel(grade: string | null): keyof typeof feedbackMessages {
  // Handle different grade formats
  if (!grade) return "2nd"; // default
  
  const gradeStr = grade.toString().toLowerCase();
  
  if (gradeStr.includes('k')) return 'kindergarten';
  if (gradeStr.includes('1')) return '1st';
  if (gradeStr.includes('2')) return '2nd';
  if (gradeStr.includes('3')) return '3rd';
  if (gradeStr.includes('4')) return '4th';
  if (gradeStr.includes('5')) return '5th';
  
  return '2nd'; // default fallback
}

// Reset message history (call at start of new practice session)
export function resetMessageHistory() {
  recentMessages = [];
}
